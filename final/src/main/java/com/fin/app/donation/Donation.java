package com.fin.app.donation;

public class Donation {
	private int donaNum;
	private int donaType;
	
	private String donaGroup;
	private int donaPrice;
	private String donaStart;
	private String donaEnd;
	private String donaState;
	
	private String mId;

	public int getDonaNum() {
		return donaNum;
	}

	public void setDonaNum(int donaNum) {
		this.donaNum = donaNum;
	}

	public int getDonaType() {
		return donaType;
	}

	public void setDonaType(int donaType) {
		this.donaType = donaType;
	}

	public String getDonaGroup() {
		return donaGroup;
	}

	public void setDonaGroup(String donaGroup) {
		this.donaGroup = donaGroup;
	}

	public int getDonaPrice() {
		return donaPrice;
	}

	public void setDonaPrice(int donaPrice) {
		this.donaPrice = donaPrice;
	}

	public String getDonaStart() {
		return donaStart;
	}

	public void setDonaStart(String donaStart) {
		this.donaStart = donaStart;
	}

	public String getDonaEnd() {
		return donaEnd;
	}

	public void setDonaEnd(String donaEnd) {
		this.donaEnd = donaEnd;
	}

	public String getDonaState() {
		return donaState;
	}

	public void setDonaState(String donaState) {
		this.donaState = donaState;
	}

	public String getmId() {
		return mId;
	}

	public void setmId(String mId) {
		this.mId = mId;
	}
	
}
